// Define a public class named "for1"
public class for1 {
    // Define the main method, the entry point of the program
    public static void main(String[] args) {
        // Start a "for" loop with an initialization of 'i' to 10,
        // a condition 'i <= 12', and an increment 'i++' to iterate through values from 10 to 12 (inclusive).
        for (int i = 10; i <= 12; i++) {
            // Within the loop, print a message indicating the remaining months,
            // with the value of 'i' included in the message.
            System.out.println("Remaining months are " + i);
        }
        // End of the "for" loop
    }
}