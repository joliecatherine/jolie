class If {
    public static void main(String args[]) {
        // Declare and initialize two integer variables 'a' and 'b'.
        int a = 10;
        int b = 15;

        // Check if 'b' is greater than 'a'.
        if (b > a) {
            // If the condition is true, print that 'b' is greater than 'a'.
            System.out.println("b is greater than a");
        } else {
            // If the condition is false, print that 'a' is greater than 'b'.
            System.out.println("a is greater than b");
        }
    }
}
